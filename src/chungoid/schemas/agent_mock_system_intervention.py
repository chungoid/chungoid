from __future__ import annotations

from typing import Any, Dict, Optional
from pydantic import BaseModel, Field

class MockSystemInterventionAgentInput(BaseModel):
    prompt_message_for_user: str = Field(..., description="The prompt message that would be shown to a human operator for system-level intervention/clarification.")
    target_context_path: Optional[str] = Field(None, description="Suggested path in context for the output. Mock may ignore this.")
    # Other fields from the planner's 'inputs' for this stage can be added if needed

class MockSystemInterventionAgentOutput(BaseModel):
    specification_output: Dict[str, Any] = Field(..., description="The mock specification data generated by the agent.")
    notes: str = Field(default="MockSystemInterventionAgent provided a predefined specification.", description="Notes from the agent execution.") 